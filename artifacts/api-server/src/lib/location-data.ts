export function getLocationStats() {
  const withNomadVisa = LOCATIONS.filter((l) => l.visa.hasDigitalNomadVisa).length;
  const lowestTax = LOCATIONS.reduce((prev, curr) =>
    (curr.tax.flatRate ?? 0.25) < (prev.tax.flatRate ?? 0.25) ? curr : prev
  );
  const highestQol = LOCATIONS.reduce((prev, curr) =>
    curr.qol.qualityOfLifeScore > prev.qol.qualityOfLifeScore ? curr : prev
  );
  const cheapest = LOCATIONS.reduce((prev, curr) => {
    const prevTotal = prev.col.monthlyRentUSD + prev.col.monthlyFoodUSD + prev.col.monthlyTransportUSD;
    const currTotal = curr.col.monthlyRentUSD + curr.col.monthlyFoodUSD + curr.col.monthlyTransportUSD;
    return currTotal < prevTotal ? curr : prev;
  });
  const regions = [...new Set(LOCATIONS.map((l) => l.region))];

  return {
    totalLocations: LOCATIONS.length,
    locationsWithNomadVisa: withNomadVisa,
    lowestTaxLocation: `${lowestTax.city}, ${lowestTax.country}`,
    highestQolLocation: `${highestQol.city}, ${highestQol.country}`,
    cheapestLocation: `${cheapest.city}, ${cheapest.country}`,
    regions,
  };
}
